function arrayBasics() {

    // Elements in an array have an order
    // The position of each element is called its index
    // The first element's index is always 0
    let batters = ['Torres', 'Judge', 'Rizzo', 'Stanton'];

    console.log(batters[0]);
    console.log(batters[2]);
    console.log(batters[4]);


    // The .length property tells us how many values are in the array
    console.log(batters.length);

    // We can use it to calculate the last index of an array
    console.log(batters[batters.length - 1]);
    console.log(batters[batters.length]);

    // Arrays are Mutable
    batters[0] = 'Spector';
    batters[batters.length - 1] = 'Salas';
    batters[10] = 'Ogbana';

    // console.log(batters);
}
arrayBasics();

